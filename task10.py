# Задаем порядок монеток (0 - герб, 1 - решка)
coins = [0, 1, 0, 1, 0, 1]

# Считаем количество монеток с гербом и количество монеток с решкой
heads_count = coins.count(0)
tails_count = coins.count(1)

# Вычисляем минимальное количество переворотов
min_flips = min(heads_count, tails_count)

print(f"Минимальное количество переворотов: {min_flips}")
